#pragma once

// Единая структура параметров генерации мира
// Используется везде для согласованности параметров
struct GeneratorParams {
	// ИСПРАВЛЕНО: очень плоский мир с большими плоскими участками
	float baseFreq = 1.0f / 2048.0f;   // ИСПРАВЛЕНО: очень длинные волны для больших плоских участков (было 1/1024)
	int octaves = 2;                   // ИСПРАВЛЕНО: уменьшено с 3 до 2 для более плавного рельефа без мелких деталей
	float lacunarity = 2.0f;
	float gain = 0.5f;
	float baseHeight = 50.0f;          // Базовый уровень земли
	float heightVariation = 1.0f;      // ИСПРАВЛЕНО: почти идеально плоский мир - только мягкие волны (было 3.0f)
	float waterLevel = 45.0f;          // Уровень воды
	int64_t seed = 1337;
};

